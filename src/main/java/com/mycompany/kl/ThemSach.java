    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        
        if(checkValiDateForm()==true){
            status.setText("Cap nhat thanh cong");
                status.setForeground(Color.red);
        }
        else{
            JOptionPane.showMessageDialog(this, "Ban chua nhap day du gia tri");
        }
        if(sb.length() > 0){
            JOptionPane.showMessageDialog(this, sb);
            return;
        }
        try{
            Sach s = new Sach();
            s.setMASACH(txtmasach.getText());
            s.setTENSACH(txttensach.getText());
            s.setTENTACGIA(txttentacgia.getText());
            s.setTHELOAI(txttheloai.getText());
            s.setTENNXB(txttennxb.getText());
            s.setSOLUONG(txtsoluong.getText());
            s.setSOLUONGCONLAI(txtsoluongconlai.getText());
            s.setSOTRANG(txtsotrang.getText());
            s.setMAVITRI(txtmavitri.getText());
            
            SachDao dao = new SachDao();
            dao.insert(s);
            
            JOptionPane.showMessageDialog(this, "Da luu thanh cong");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

