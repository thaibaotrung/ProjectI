    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        try{
            String url = "jdbc:mysql://localhost/quanlysach";
        var user = "root";
        var password = "";
             Connection con = DriverManager.getConnection(url, user, password);
            
            String sql = "delete from sach where MASACH = '"+txtmasach.getText()+"'";
            Statement st = (Statement) con.createStatement();
            int chk = JOptionPane.showConfirmDialog(this, "Ban chac chan xoa chu!", "Thong bao!", JOptionPane.YES_NO_OPTION);
            if(chk == JOptionPane.YES_OPTION){
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Xoa thanh cong!");
            }
        }catch(Exception e){
            e.printStackTrace();
                JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btndeleteActionPerformed

